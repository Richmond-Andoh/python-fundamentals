#Dictionaries

my_dic = {
    'name': 'John',
    'age': 30,
    'city': 'New York',
    'is_tall': True,
    'is_married': False,
    'children': ['Mary', 'Joe', 'Jim']
}

#print the whole my_dic
print(my_dic)

#print only the values using their keys
print(my_dic['name'])
print(my_dic['age'])
print(my_dic['is_tall'])
print(my_dic['is_married'])




